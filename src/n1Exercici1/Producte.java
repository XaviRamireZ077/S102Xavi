package n1Exercici1;

public class Producte {

	//Atributs
	
	private String nom;
	private double preu;
	
	
	
	//Constructor
	
	public Producte(String nom, double preu) {
		super();
		this.nom = nom;
		this.preu = preu;
	}

    //Getters i Setters

	public String getNom() {
		return nom;
	}



	public void setNom(String nom) {
		this.nom = nom;
	}



	public double getPreu() {
		return preu;
	}



	public void setPreu(double preu) {
		this.preu = preu;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
